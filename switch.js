let day = "Friday" ;

switch(day) {
    case "Saturday":
        console.log("First day at office after offday in a week...");
        break;
    case "Thursday":
        console.log("Last day at office before offday in a week...");
        break;
    case "Friday":
        console.log("Offday at office in every week...");
        break;
    default:
        console.log("Mid-working day at the office...")
}